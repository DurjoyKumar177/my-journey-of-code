# Basic Input and Output in Python

# Taking input from the user
name = input("Enter your name: ")
age = input("Enter your age: ")
University = "Dhaka City College" #default value for University

# Displaying output
print("Hello,", name + "!")
print("I am", age, "years old.")
print("I study at", University + ".")

# Using f-string for formatted output
print(f"Welcome! I am {name}, I am {age} years old and I study at {University}.")